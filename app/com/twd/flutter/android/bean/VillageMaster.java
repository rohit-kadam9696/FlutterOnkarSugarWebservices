package com.twd.flutter.android.bean;

public class VillageMaster {
    private int nvillageId;
    private String villageNameLocal;
    private int nsectionId;
    private String nvillType;
    private String vlat;
    private String vlong;
    private String vradius;

    public int getNvillageId() {
        return nvillageId;
    }

    public void setNvillageId(int nvillageId) {
        this.nvillageId = nvillageId;
    }

    public String getVillageNameLocal() {
        return villageNameLocal;
    }

    public void setVillageNameLocal(String villageNameLocal) {
        this.villageNameLocal = villageNameLocal;
    }

    public int getNsectionId() {
        return nsectionId;
    }

    public void setNsectionId(int nsectionId) {
        this.nsectionId = nsectionId;
    }

    public String getNvillType() {
        return nvillType;
    }

    public void setNvillType(String nvillType) {
        this.nvillType = nvillType;
    }

    public String getVlat() {
        return vlat;
    }

    public void setVlat(String vlat) {
        this.vlat = vlat;
    }

    public String getVlong() {
        return vlong;
    }

    public void setVlong(String vlong) {
        this.vlong = vlong;
    }

    public String getVradius() {
        return vradius;
    }

    public void setVradius(String vradius) {
        this.vradius = vradius;
    }
}
