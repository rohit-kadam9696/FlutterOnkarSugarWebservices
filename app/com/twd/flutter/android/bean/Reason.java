package com.twd.flutter.android.bean;

public class Reason {
    private int nreasonId;
    private int nreasonGroupId;
    private String vreasonName;

    public int getNreasonId() {
        return nreasonId;
    }

    public void setNreasonId(int nreasonId) {
        this.nreasonId = nreasonId;
    }

    public int getNreasonGroupId() {
        return nreasonGroupId;
    }

    public void setNreasonGroupId(int nreasonGroupId) {
        this.nreasonGroupId = nreasonGroupId;
    }

    public String getVreasonName() {
        return vreasonName;
    }

    public void setVreasonName(String vreasonName) {
        this.vreasonName = vreasonName;
    }

}
